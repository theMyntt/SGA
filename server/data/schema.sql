DROP DATABASE IF EXISTS USERS;

CREATE DATABASE IF NOT EXISTS USERS;

USE USERS;

CREATE TABLE IF NOT EXISTS SCHOOL_INFORMATION (
		ID_SCHOOL INTEGER AUTO_INCREMENT NOT NULL,
        NAME_SCHOOL VARCHAR(100) NOT NULL,
        CELLPHONE_SCHOOL VARCHAR(11) NOT NULL,
        PRIMARY KEY (ID_SCHOOL)
);

CREATE TABLE IF NOT EXISTS USER_INFORMATION (
		ID_USER INTEGER AUTO_INCREMENT NOT NULL,
        EMAIL_USER VARCHAR(90) NOT NULL,
        PASSWORD_USER VARCHAR(30) NOT NULL,
        CELLPHONE_USER VARCHAR(11),
        NAME_USER VARCHAR(80) NOT NULL,
        SCHOOL_ID_SCHOOL INTEGER NOT NULL,
        PRIMARY KEY (ID_USER),
        FOREIGN KEY (SCHOOL_ID_SCHOOL) REFERENCES SCHOOL_INFORMATION(ID_SCHOOL)
);

